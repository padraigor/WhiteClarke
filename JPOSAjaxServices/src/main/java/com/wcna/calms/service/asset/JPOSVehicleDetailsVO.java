package com.wcna.calms.service.asset;

import com.wcna.calms.jpos.services.asset.IJPOSVehicleDetailsVO;

public class JPOSVehicleDetailsVO  implements IJPOSVehicleDetailsVO {

	private String registrationDate;
	private String registrationNumber;
	private String vin;
	private boolean isConfirmSave;
	private String colour;
	private String fuelType;
	private String assetSellerCode;
	private long meterValue;
	private String mortgageRegDate;

	public String getRegistrationDate() {
		return registrationDate;
	}
	public void setRegistrationDate(String registrationDate) {
		this.registrationDate = registrationDate;
	}
	public String getRegistrationNumber() {
		return registrationNumber;
	}
	public void setRegistrationNumber(String registrationNumber) {
		this.registrationNumber = registrationNumber;
	}
	public String getVin() {
		return vin;
	}
	public void setVin(String vin) {
		this.vin = vin;
	}

	public boolean isConfirmSave() {
		return isConfirmSave;
	}
	public void setConfirmSave(boolean isConfirmSave) {
		this.isConfirmSave = isConfirmSave;
	}
	public void reset() {
		this.registrationDate = "";
		this.registrationNumber = "";
//		this.startDate = "";
		this.vin = "";

	}
	public String getColour() {
		return colour;
	}
	public void setColour(String colour) {
		this.colour = colour;
	}
	public String getAssetSellerCode() {
		return assetSellerCode;
	}
	public void setAssetSellerCode(String assetSellerCode) {
		this.assetSellerCode = assetSellerCode;
	}
	public String getFuelType() {
		return fuelType;
	}
	public void setFuelType(String fuelType) {
		this.fuelType = fuelType;
	}
	public long getMeterValue()
    {
        return this.meterValue;
    }

    public void setMeterValue(long meterValue)
    {
        this.meterValue = meterValue;
    }
	public String getMortgageRegDate() {
		return mortgageRegDate;
	}
	public void setMortgageRegDate(String mortgageRegDate) {
		this.mortgageRegDate = mortgageRegDate;
	}



}
